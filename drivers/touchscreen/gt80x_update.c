static uint16_t gt80x_to_write_addr(uint16_t ReadAddress)
{
    uint16_t Guitar_WriteAddressTemp;

    uint16_t Guitar_WriteBlockReal;
    uint16_t Guitar_WriteBlockTheor;

    Guitar_WriteBlockReal = 4 * ((ReadAddress / 2048) % 8) + (ReadAddress / 2) % 4;
    Guitar_WriteBlockTheor = 5 * (Guitar_WriteBlockReal % 4) + Guitar_WriteBlockReal / 4;
    Guitar_WriteAddressTemp = 2048 * (Guitar_WriteBlockTheor / 4) + 2 * (Guitar_WriteBlockTheor % 4) + 8 * ((ReadAddress / 8) % 256);

    return Guitar_WriteAddressTemp;
}

//擦除地址数据
static bool gt80x_erase_addr(u16 Address)
{
    uint8_t Guitar_TimeOut = 0;
    uint8_t tmp = 1;

    Guitar_WriteBuf[0] = 0x03;
    Guitar_WriteBuf[1] = (Address >> 8) & 0xff;	//地址高位
    Guitar_WriteBuf[2] = Address & 0xff;		//地址低位
    i2c_write_bytes(0x50, Guitar_WriteBuf, 3);

    Guitar_WriteBuf[0] = 0x00;
    Guitar_WriteBuf[1] = 0x01 ;//擦除命令
    i2c_write_bytes(0x50, Guitar_WriteBuf, 2);
    msleep(10);
    Guitar_ReadBuf[0] = 0x00;
    Guitar_ReadBuf[1] = 0x01;
    i2c_read_bytes(0x50, Guitar_ReadBuf, 2);

    tmp = Guitar_ReadBuf[1];
    while(tmp != 0)
    {
        msleep(5);
        i2c_read_bytes(0x50, Guitar_ReadBuf, 2);
        tmp = Guitar_ReadBuf[1];

        Guitar_TimeOut++;

        if(Guitar_TimeOut > 30) //最长擦除需要20ms
        {
            debug_printk(LEVEL_DEBUG, "Erase Timeout!\n");
            return false;
        }
    }
    return true;
}


