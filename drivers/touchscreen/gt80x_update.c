//与EEPROM 代码通信，写入RAM代码
static bool gt80x_write_ram_ctrl_code(void)
{
    int ret, i;
    u16 ram_chk_sum = 0;
    u8 data_buff[251];
    u8 buffer[3];
    u8 retry_time = 3;

    for(i = 0; i < 3; i++)
    {
        //准备一帧数据
        data_buff[0] = 0x00;
        memcpy(data_buff + 1, gt80x_ram_ctrl_code + 250 * i, 250);

        //清空数据准备好标志
        buffer[0] = 0xFD;
        buffer[1] = 0x00;
        ret = i2c_write_bytes(0xA8 >> 1, buffer, 2);

        //清空数据搬完标志
        buffer[0] = 0xFF;
        ret = i2c_write_bytes(0xA8 >> 1, buffer, 2);

        //准备帧号
        buffer[0] = 0xFE;
        buffer[1] = (uint8_t)i;
        ret = i2c_write_bytes(0xA8 >> 1, buffer, 2);

        //发送一帧数据
        ret = mtk_i2c_write_bytes(0xA8 >> 1, data_buff, 251);

        //置上数据准备好标志
        //tmp = 0x80;
        buffer[0] = 0xFD;
        buffer[1] = 0x80;
        ret = i2c_write_bytes(0xA8 >> 1, buffer, 2);

        msleep(10);

        buffer[0] = 0xFF;
        buffer[1] = 0x00;
        while(retry_time)
        {
            //读取搬代码完成标志
            ret = i2c_read_bytes(0xA8 >> 1, buffer, 2);
            if((ret > 0) && (buffer[1] == 0x80))
            {
                break;
            }

            retry_time--;
            if(retry_time == 0)
            {
                debug_printk(LEVEL_INFO, "Write control-code failed. ret: %d, data: 0x%02x\n", ret, buffer[1]);

                gt80x_stdpin_set_high();
                msleep(10);
                gt80x_stdpin_set_low();
                msleep(30);
                return false;
            }
        }
    }

    //发送全部完成标志
    buffer[0] = 0xFE;
    buffer[1] = 0xFF;
    ret = i2c_write_bytes(0xA8 >> 1, buffer, 2);

    //等待RAM完成自身校验操作
    msleep(100);

    //读取校验
    ret = i2c_read_bytes(0xA8 >> 1, buffer, 3);
    ram_chk_sum = (buffer[1] << 8) + buffer[2];

    debug_printk(LEVEL_INFO, "Read control-code checksum, ret: %d, data:0x%04x\n", ret, ram_chk_sum);
    if((ret > 0) && (ram_chk_sum == 0xF173))//为ram check sum
        return true;
    else
        return false;
}

//读取烧录完成标志
static uint8_t read_frame_finishflag(uint8_t Guitar_RambufNumber)
{
    Guitar_ReadBuf[0] = 0x54 + Guitar_RambufNumber * 0x80;
    Guitar_ReadBuf[1] = 0x00;
    i2c_read_bytes(0x54, Guitar_ReadBuf, 2);
    return Guitar_ReadBuf[1];
}

//功能:通过I2C发送一帧数据给RAM
static bool send_frame_data(uint8_t *pdata, uint8_t Guitar_RamAddress, uint8_t Guitar_FrameNumber)
{
    int ret = -1;
    uint8_t i = 0;
    uint8_t read_data[81];

    //发送80字节数据
    pdata[0] = Guitar_RamAddress;
    ret = mtk_i2c_write_bytes(0x54, pdata, 81);
    if(ret <= 0)
        return false;

    //检查发送数据是否正确
    read_data[0] = Guitar_RamAddress;
    ret = mtk_i2c_read_bytes(0x54, read_data, 81);
    for(i = 0; i < 81; i++)
    {
        if(read_data[i]  != pdata[i])
            return false;
    }

    //发送数据准备标志
    Guitar_WriteBuf[0] = Guitar_RamAddress + 0x50 + 2;
    Guitar_WriteBuf[1] = 0x80 | Guitar_FrameNumber;
    ret = i2c_write_bytes(0x54, Guitar_WriteBuf, 2);
    return ret > 0 ? true : false;
}

/*******************************************************
功能：
	烧写ROM代码
参数：
	path: 待升级文件的路径
return：
	执行结果码，1表示烧写成功
*******************************************************/
static int gt80x_write_firmware(void)
{
    bool ret = false;
    uint8_t i = 0;
    uint16_t timeout = 0;
    uint16_t write_flag = 0;
    //存放代码，首地址为待写入地址
    uint8_t update_code_buffer[81];

    debug_printk(LEVEL_DEBUG, "Please wait for chip formatting, about 30s or more.\n");

    msleep(30000);	//wait for EEPROM format action finished.

    debug_printk(LEVEL_INFO, "Start to write the EEPROM code.\n");

    for(i = 0; i <= 128; i++)
    {
        //send frame data
        ret = false;
        timeout = 0;
        while(!ret && i != 128)
        {
            timeout++;
            if(timeout > 1000)
            {
                debug_printk(LEVEL_WARN, "Send frame falied.\n");
                return -ERROR_TIMEOUT;
            }

            memcpy(update_code_buffer + 1, gt80x_update_code + 80 * i, 80);
            ret = send_frame_data(update_code_buffer, (i % 2) * 0x80, i); //该帧烧录完成,发送下一帧数据到前两帧的缓冲区
            msleep(5);
        }

        //first frame and second frame are sent together.
        if(i == 0)
            continue;

        //check if write finished.
        timeout = 0;
        do  	//等待前一帧写入完成
        {
            timeout++;
            if(timeout > 2000)
            {
                debug_printk(LEVEL_WARN, "Write frame falied.\n");
                return -ERROR_TIMEOUT;
            }

            write_flag = read_frame_finishflag((i - 1) % 2);
            msleep(5);
        }
        while(write_flag != (0x80 | (i - 1)));
        debug_printk(LEVEL_INFO, "Frame:%d.\n", i);
    }

    msleep(1000);
    return 1;

}

//读取Firmware的Checksum
static uint16_t gt80x_read_checksum(void)
{
    int ret = -1;
    uint16_t Guitar_CheckSumTemp = 0;

    gt80x_stdpin_set_high();
    msleep(5);          //5
    gt80x_stdpin_set_low();

    msleep(50);
    debug_printk(LEVEL_DEBUG, "Write Command to calculate checksum about firmware.\n");

    Guitar_WriteBuf[0] = 0x69;
    Guitar_WriteBuf[1] = 0x07;
    ret = i2c_write_bytes(0x55, Guitar_WriteBuf, 2);
    if(ret < 0)
        return 0;

    //延时5S等待guitar程序计算checksum
    msleep(5000);

    Guitar_ReadBuf[0] = 0x6A;
    Guitar_ReadBuf[1] = 0x00;
    Guitar_ReadBuf[2] = 0x00;
    i2c_read_bytes(0x55, Guitar_ReadBuf, 3);   //0x55
    Guitar_CheckSumTemp = ((uint16_t)Guitar_ReadBuf[1] << 8) + Guitar_ReadBuf[2];
    return 	Guitar_CheckSumTemp;
}

/*******************************************************
功能：
	读取文件中的校验码，对烧写好的ROM代码进行校验
return：
	执行结果码，1表示校验成功
*******************************************************/
static bool gt80x_check_checksum(bool show_tips)
{
    int count;
    bool ret = false;
    uint16_t file_checksum = 0, rom_checksum = 0;
    const uint8_t *Guitar_FileBuffer = gt80x_update_code;

    debug_printk(LEVEL_INFO, "Check the EEPROM code.\n");

    for(count = 0; count < 10240; count++)
        file_checksum += (uint16_t) * Guitar_FileBuffer++;

    rom_checksum = gt80x_read_checksum();//校验程序
    debug_printk(LEVEL_DEBUG, "File CheckSum:0x%04x, Rom Check_Sum:0x%04x\n", file_checksum, rom_checksum);
    ret = (file_checksum == rom_checksum) ? true : false;

    if(show_tips)
        debug_printk(LEVEL_INFO, "The Updating code of Guitar has finished. It is %s.\n", ret ? "successful." : "failed.");

    return ret;
}


/*读取GT80X的版本号并打印*/
static bool  goodix_read_version(uint8_t *buffer)
{
    int ret;
    uint8_t version[2] = {0x69, 0xff};					//command of reading Guitar's version
    uint8_t version_data[GT80X_VERSION_LENGTH + 1] = {0x6A};	//store touchscreen version infomation
    memset(version_data + 1, 0, GT80X_VERSION_LENGTH);
    if(buffer != NULL)
        memset(buffer, 0, GT80X_VERSION_LENGTH);
    ret = i2c_write_bytes(0x55, version, 2);
    if (ret <= 0)
        goto error_go_out;

    msleep(50);
    ret = mtk_i2c_read_bytes(0x55, version_data, GT80X_VERSION_LENGTH);
    if (ret <= 0)
        goto error_go_out;

    if(buffer != NULL)
        memcpy(buffer, version_data + 1, GT80X_VERSION_LENGTH);
    version[1] = 0x00;				//cancel the command
    i2c_write_bytes(0x55, version, 2);
    return true;

error_go_out:
    if(buffer != NULL)
        strncpy(buffer, "NULL", 4);
    return false;
}

//比较升级文件版本和ROM固件版本，VERSION_INVALID表明IC与Code版本不一致
static int check_version(char *rom, const char *file)
{
    uint8_t  count;
    char *p = rom;
    assert(file && rom);
    if(file == NULL || rom == NULL)
        return -EFAULT;

    if(*rom == '\0')	//读取内容为空串
        return VERSION_CONFUSION;

    debug_printk(LEVEL_DEBUG, "Rom:%s, File:%s\n", rom, file);
    count = 0;
    do 					//检查非法字符和空串
    {
        if((*p > 122) || ( *p < 48 && *p != 32) || (*p > 57 && *p < 65)
                || (*p > 90 && *p < 97 && *p  != '_'))
            count++;
    }
    while(*++p != '\0');
    if (count >= 2)
        return VERSION_CONFUSION;

    p = rom;	/*Is GT800 or GT801?*/
    if (strncmp(file, p, 6) == 0)
    {
        if(strncmp(file, p, 10) > 0)
            return VERSION_HIGHER;	//文件版本较新
        if(strncmp(file, p, 10) == 0)
            return VERSION_EQUAL;
        else
            return VERSION_LOWER; 	//文件版本相同或较低
    }
    else if (strncmp(p, GT80X_CHIP_TYPE_0, 5) == 0 || strncmp(p, GT80X_CHIP_TYPE_1, 5) == 0)
    {
        if(file[4] != p[4])
            return VERSION_INVALID;	//文件版本与芯片版本不一致
    }

    return VERSION_CONFUSION;
}

/*******************************************************
功能：
	测试IC Firmware状态，判断是否需要进行升级操作
return：
	true表明不需要进行升级或无法进行升级，false为需要进行升级
*******************************************************/
static int gt80x_check_firmware(void)
{
    int retry = 0;
    int ret = false;
    bool checksum_state = false;	//CheckSum检查状态
    uint8_t gt80x_rom_version[GT80X_VERSION_LENGTH + 2] = { 0 };

    goodix_ts_set(GOODIX_TS_STOP);

    gt80x_stdpin_set_high();
    msleep(10);
    gt80x_stdpin_set_low();
    msleep(30);
    // 测试0xAA通信
    for(retry = 0; retry < 10; retry++)
    {
        ret = gt80x_i2c_test(0x55);
        if(ret)
            break;
    }
    debug_printk(LEVEL_INFO, "I2C test is %s in address: 0xAA.\n", ret ? "successful" : "failed");
    if(!ret)
        goto error_go_out;

    //Slave address 0xaa is connected, so it can be reading checksum and version.
    //Only if all check (5 or more times) is failed.
    for(retry = 0; retry < 5; retry++)
    {
        ret = gt80x_check_checksum(false);
        if(ret)
        {
            checksum_state = true;
            break;
        }
    }

    gt80x_stdpin_set_high();
    msleep(10);
    gt80x_stdpin_set_low();
    msleep(50);
    for(retry = 0; retry < 10; retry++)
    {
        ret = goodix_read_version(gt80x_rom_version);

        //Version data Must be match with pattern such as "GT801" or "GT800", and they can't be replaced by each other.
        debug_printk(LEVEL_INFO, "Guitar rom version:%s\n", gt80x_rom_version);
        if(strncmp(gt80x_rom_version, "NULL", 4) == 0)
            memset(gt80x_rom_version, 0, GT80X_VERSION_LENGTH);

        ret = check_version(gt80x_rom_version, gt80x_file_version);
        if(checksum_state && (ret == VERSION_EQUAL))
        {
            //firmware version MUST be equal to file code.
            debug_printk(LEVEL_INFO, "Version data is matched, Everything is OK.\n");
            break;
        }
        if(ret == VERSION_INVALID)
            break;
    }
    if(!checksum_state)
        debug_printk(LEVEL_WARN, "ROM check failed, GT80X's firmware must be not correct.\n");

    if(ret == VERSION_INVALID)
    {
        checksum_state = true;
        //Please check the Chip type.
        debug_printk(LEVEL_WARN, "The file is not match Firmware Type. Is GT800 or GT801?\n");
    }
    else if(ret != VERSION_EQUAL)
        checksum_state = false;

    version_check = ret;
    //checksum_state = false; /*Only for debug*/
error_go_out:
    goodix_ts_set(GOODIX_TS_CONTINUE);
    return checksum_state;
}


