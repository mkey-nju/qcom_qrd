//擦除一段EEPROM
static bool gt80x_erase_eeprom(void)
{
    int ret;
    int i, j;
    for(i = 0; i < (sizeof(gt80x_eeprom_code) + 1) / 2; i++)
    {
        ret = 0;
        for(j = 0; j < 5; j++)
            ret += gt80x_erase_addr(gt80x_to_write_addr(0x4000 + 2 * i));

        if(ret == 0)
        {
            debug_printk(LEVEL_ERROR, "Erase addr 0x%04x failed.\n", 0x4000 + 2 * i);
            return false;
        }
    }

    return true;
}

//写入中转的EEPROM代码
static bool gt80x_write_eeprom_code(void)
{
    int ret;
    int i, j;

    for(i = 0; i < sizeof(gt80x_eeprom_code) + 1; i += 2)
    {
        ret = 0;
        for(j = 0; j < 5; j++)
            ret += gt80x_write_word(gt80x_to_write_addr(0x4000 + i), gt80x_eeprom_code[i] * 256 + gt80x_eeprom_code[i + 1]);

        if(ret == 0)
        {
            debug_printk(LEVEL_ERROR, "Write addr 0x%04x failed.\n", 0x4000 + i);
            return false;
        }
    }

    //Reset chip.
    gt80x_stdpin_set_high();

    msleep(10);

    gt80x_stdpin_set_low();

    msleep(100);

    return true;
}

